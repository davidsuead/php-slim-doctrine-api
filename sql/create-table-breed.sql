CREATE TABLE IF NOT EXISTS breed(
	id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	breed_adaptability SMALLINT,
	breed_affection_level SMALLINT,
	breed_alt_names VARCHAR(100),
	breed_cfa_url VARCHAR(100),
	breed_child_friendly SMALLINT,
	breed_country_code VARCHAR(100),
	breed_country_codes VARCHAR(100),
	breed_description TEXT,
	breed_dog_friendly SMALLINT,
	breed_energy_level SMALLINT,
	breed_experimental SMALLINT,
	breed_grooming SMALLINT,
	breed_hairless SMALLINT,
	breed_health_issues SMALLINT,
	breed_hypoallergenic SMALLINT,
	breed_breed_id VARCHAR(100),
	breed_indoor SMALLINT,
	breed_intelligence SMALLINT,
	breed_lap SMALLINT,
	breed_life_span VARCHAR(100),
	breed_name VARCHAR(100),
	breed_natural SMALLINT,
	breed_origin VARCHAR(100),
	breed_rare SMALLINT,
	breed_reference_image_id VARCHAR(100),
	breed_rex SMALLINT,
	breed_shedding_level SMALLINT,
	breed_short_legs SMALLINT,
	breed_social_needs SMALLINT,
	breed_stranger_friendly SMALLINT,
	breed_suppressed_tail SMALLINT,
	breed_temperament VARCHAR(100),
	breed_vcahospitals_url VARCHAR(100),
	breed_vetstreet_url VARCHAR(100),
	breed_vocalisation SMALLINT,
	breed_wikipedia_url SMALLINT,
	breed_weight_imperial VARCHAR(100),
	breed_weight_metric VARCHAR(100)
);